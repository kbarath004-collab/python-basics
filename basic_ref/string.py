''' String Slicing (Indexing)''' 

x = "Hello, World!"
print(x[2:6]) # this will print characters from index 2 to 4 (5 is not included)

print(x[:4]) # this will print characters from the beginning to index 4

print(x[2:]) # this will print characters from index 2 to the end

print(x[-5:-2]) # this will print characters from index -5 to -3 (negative indexing)

